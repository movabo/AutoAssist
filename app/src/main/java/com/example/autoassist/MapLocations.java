    /**
     * Maximum distance until two locations are considered close to each other. (in km)
     */
    public static final double IS_CLOSE_WHEN_DISTANCE_BELOW = 0.1;

    /**
     * Approx. earth radius in km.
     */
    private static final int EARTH_RADIUS = 6371;

    /**
     * Whether two points are close to another.
     *
     * @param latitude1 Latitude of the first point to measure whether its close to the second.
     * @param longitude1 Longitude of the first point to measure whether its close to the second.
     * @param latitude2 Latitude of the second point to measure whether its close to the first.
     * @param longitude2 Longitude of the second point to measure whether its close to the first.
     * @return Whether the two points are close.
     */
    static boolean isCloseTo(double latitude1, double longitude1, double latitude2, double longitude2) {
        return distance(latitude1, longitude1, latitude2, longitude2) < IS_CLOSE_WHEN_DISTANCE_BELOW;
    }

    /**
     * Whether location is close to any location in locations.
     *
     * @param location Location to check against the other locations.
     * @param locations Locations (tuple of lat, lon) to check against location.
     * @return Whether location is close to any of the other locations.
     */
    static boolean isCloseToAny(Location location, double[][] locations) {
        for (double[] current: locations) {
            if (isCloseTo(location.getLatitude(), location.getLongitude(), current[0], current[1])) {
                return true;
            }
        }
        return false;
    }

    /*
     * Credit for the next two methods to:
     *
     * Jason Winn
     * http://jasonwinn.org
     * Created July 10, 2013
     *
     * Description: Small class that provides approximate distance between
     * two points using the Haversine formula.
     *
     * Call in a static context:
     * Haversine.distance(47.6788206, -122.3271205,
     *                    47.6788206, -122.5271205)
     * --> 14.973190481586224 [km]
     *
     * Source: https://github.com/jasonwinn/haversine/blob/master/Haversine.java
     */

    /**
     * Approximate distance between two points using the Haversine formula.
     * Source: https://github.com/jasonwinn/haversine/blob/master/Haversine.java
     *
     * @param latitude1 Latitude of the first point to measure the distance to the second.
     * @param longitude1 Longitude of the first point to measure the distance to the second.
     * @param latitude2 Latitude of the second point to measure the distance to the first.
     * @param longitude2 Longitude of the second point to measure the distance to the first.
     * @return Approximate distance between two points in km.
     */
    public static double distance(double latitude1, double longitude1,
                                  double latitude2, double longitude2) {

        double dLat  = Math.toRadians((latitude2 - latitude1));
        double dLong = Math.toRadians((longitude2 - longitude1));

        latitude1 = Math.toRadians(latitude1);
        latitude2   = Math.toRadians(latitude2);

        double a = haversin(dLat) + Math.cos(latitude1) * Math.cos(latitude2) * haversin(dLong);
        double c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));

        return EARTH_RADIUS * c; // <-- d
    }

    public static double haversin(double val) {
        return Math.pow(Math.sin(val / 2), 2);
    }

    /**
     * Whether the location is close to any store in Hesse, Germany.
     *
     * @param location Location to check if close to any store.
     * @return Whether the location is close to any store.
     */
    static boolean isCloseToStore(Location location) {
        return isCloseToAny(location, STORES);
    }

